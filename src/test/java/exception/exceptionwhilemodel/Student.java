package exception.exceptionwhilemodel;

public class Student {

	private String name;
	
	private int age;
	
	public Student(String _name){
		this.name=_name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
}
